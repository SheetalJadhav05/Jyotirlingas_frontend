// Search function
export function searchJyotirlinga(query) {
  const filter = query.toUpperCase();
  return jyotirlingasData.filter(
    (jyotirlinga) =>
      jyotirlinga.name.toUpperCase().includes(filter) ||
      jyotirlinga.location.toUpperCase().includes(filter) ||
      jyotirlinga.state.toUpperCase().includes(filter),
  );
}

export function getJyotirlingaById(id) {
  return jyotirlingasData.find((j) => j.id === id);
}

export function getAllJyotirlingas() {
  return jyotirlingasData;
}
